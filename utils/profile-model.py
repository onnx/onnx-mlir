# Sample init .cpp printed by `--help init`. This mirrors the granite
# example shipped alongside the script — keep them in sync if the input
# shape conventions ever change. The point is to give the user a
# copy-paste-and-edit starting point: change shape/dtype/values to
# match their model's input contract; the function signature must stay
# `extern "C" OMTensorList *initialize_model_input(void)`.
SAMPLE_INIT_CPP = r"""// Example initialize_model_input() — adapt the shapes, dtypes, and
// content to your own model. The driver calls this once before the
// timing loop and reuses the returned OMTensorList* for every
// iteration. The buffers must outlive the entire process; `static`
// arrays inside the function body are the simplest way to guarantee
// that without an owning wrapper around OMTensor (omTensorCreate
// borrows the pointer; it does not copy).
//
// This particular example is for granite-embedding-278m-multilingual,
// shape-info 0:1x180,1:1x180:
//   input_ids       int64[1,180]  random in [0, 30000)
//   attention_mask  int64[1,180]  all ones

#include <cstdint>
#include <random>

#include <OnnxMlirRuntime.h>

extern "C" OMTensorList *initialize_model_input(void) {
  constexpr int64_t kBatch = 1;
  constexpr int64_t kSeqLen = 180;
  constexpr int64_t kRank = 2;
  constexpr int64_t kNumElems = kBatch * kSeqLen;

  static int64_t shape[2] = {kBatch, kSeqLen};
  static int64_t inputIds[kNumElems];
  static int64_t attentionMask[kNumElems];

  // Fixed seed → bit-identical repeat runs. The profiler doesn't care
  // about the values, only the shape of the workload.
  std::mt19937_64 rng(12345);
  std::uniform_int_distribution<int64_t> vocab(0, 29999);
  for (int64_t i = 0; i < kNumElems; ++i) {
    inputIds[i] = vocab(rng);
    attentionMask[i] = 1;
  }

  OMTensor *idsT =
      omTensorCreate(inputIds, shape, kRank, ONNX_TYPE_INT64);
  OMTensor *maskT =
      omTensorCreate(attentionMask, shape, kRank, ONNX_TYPE_INT64);
  OMTensor *list[2] = {idsT, maskT};
  return omTensorListCreate(list, 2);
}
"""

# OnnxMlirRuntime.h lives at <repo>/include/OnnxMlirRuntime.h and this
# script ships at <repo>/utils/<name>.py — derive the include directory
# rather than hard-coding so a moved checkout still builds.
DEFAULT_INCLUDE_DIR = os.path.normpath(os.path.join(SCRIPT_DIR, "..", "include"))


def compile_workload(init_cpp, model_so, verbose=False, extra_cflags=None):
    """Compile the embedded workload + the user's init .cpp + model.so
    into a single static binary. Returns the binary path.

    Compile command (rebuilt from defaults each call):
      clang++ -std=c++17 -O2 -g -I <include-dir>
              <workload.cpp> <init.cpp> <model.so>
              -o <binary>
    Extra clang flags can be appended via `extra_cflags` (a list).
    """
    # Materialize the embedded workload .cpp to disk so clang can ingest
    # it. Overwrite each run so any edits to this script's WORKLOAD_CPP
    # take effect on the next build.
    with open(WORKLOAD_CPP_PATH, "w") as f:
        f.write(WORKLOAD_CPP)

    so_abs = os.path.abspath(model_so)
    so_dir = os.path.dirname(so_abs)
    cmd = [
        "clang++",
        "-std=c++17",
        "-O2",
        "-g",
        "-I",
        DEFAULT_INCLUDE_DIR,
        WORKLOAD_CPP_PATH,
        os.path.abspath(init_cpp),
        so_abs,
    ]
    if IS_LINUX:
        # On Linux, the ELF linker by default records the .so filename
        # (its DT_NEEDED) without any path. Embed an rpath pointing at
        # the .so's directory so dyld/ld.so finds it at process start
        # without the user having to pre-set LD_LIBRARY_PATH. We still
        # set LD_LIBRARY_PATH at run time as a belt-and-suspenders.
        # `-ldl` is needed for dladdr() used by the in-process sampler
        # to resolve model.so's runtime base; harmless on builds that
        # don't end up using --sampler inproc.
        cmd += ["-L", so_dir, "-Wl,-rpath," + so_dir, "-ldl"]
    cmd += ["-o", DEFAULT_BINARY_PATH]
    if extra_cflags:
        cmd[1:1] = list(extra_cflags)  # insert just after clang++

    print(f"Compiling: workload + {init_cpp}")
    if verbose:
        print(f"  $ {' '.join(cmd)}")
    r = subprocess.run(cmd, capture_output=True, text=True)
    if r.returncode != 0:
        print("--- compile stdout ---")
        print(r.stdout)
        print("--- compile stderr ---")
        print(r.stderr)
        sys.exit(f"Compile failed (exit {r.returncode}).")
    if verbose and r.stderr:
        print(r.stderr)
    if not os.path.exists(DEFAULT_BINARY_PATH):
        sys.exit(f"Compile succeeded but binary not found: " f"{DEFAULT_BINARY_PATH}")
    print(f"Built: {DEFAULT_BINARY_PATH}")
    return DEFAULT_BINARY_PATH


