#!/usr/bin/env python3
"""
v2 driver — the inference workload .cpp (main(), the invoke_run wrapper,
argv parsing, the timing loop) is embedded in this script as a string
constant. The user only writes one function:

    extern "C" OMTensorList *initialize_model_input(void);

It is called ONCE before the timing loop starts and the returned tensor
list is reused for every invoke_run iteration. Buffer lifetime is the
function's responsibility — typically `static` arrays inside the function.

Everything else (per-binary leaf samples, per-frame attribution,
instruction-mix histogram, per-ONNX-op breakdown via OMInstrumentPoint
markers) is identical to v1.

How "inside the model" is detected
----------------------------------
The embedded workload defines a small `invoke_run()` wrapper around
`run_main_graph` and is built with `-g` so the symbol is visible in
`sample` output. A stack is "inside the model" iff `invoke_run` appears
anywhere as an ancestor frame.

Pipeline
--------
  1. Compile the embedded workload .cpp + the user's init .cpp +
     model.so into a single static binary (clang++, -O2 -g, linking
     the model.so directly so dyld will load it at process start).
  2. Spawn the binary with `-t <duration+10>` so the workload outlasts
     the sampling window. DYLD_LIBRARY_PATH points at the .so's
     directory in case its install_name is just a basename.
  3. Run macOS `sample <pid> <duration> -f <out>`.
  4. Walk the call graph: total samples, inside-marker samples,
     per-binary leaves, top frames.
  5. Disassemble model.so and produce the instruction-mix histogram
     (and, if the .so was built with --profile-ir, the per-ONNX-op
     attribution via OMInstrumentPoint markers).
  6. Write a JSON profile and print the summary.

"""

