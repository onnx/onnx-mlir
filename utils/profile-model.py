def _so_arch(so_path):
    """Return a short architecture name for the .so's machine type
    ('arm64', 'x86_64', 's390x', 'ppc64', or 'unknown').

    Used to gate the per-ONNX-op OMInstrumentPoint dataflow recovery —
    the adrp+add+mov chain it walks is arm64-specific. Each non-arm64
    target would need its own pattern (x86_64: RIP-relative LEA + reg
    moves; s390x: LARL + LA; ppc64: addis+addi). Until those are
    written, all non-arm64 paths skip per-op recovery and surface the
    arch in the skip message so the user knows what's needed.
    """
    r = subprocess.run(["file", so_path], capture_output=True, text=True)
    out = (r.stdout or "").lower()
    # `file`'s wording varies by version; match generously.
    if "aarch64" in out or "arm64" in out:
        return "arm64"
    if "x86-64" in out or "x86_64" in out or "x86 64" in out:
        return "x86_64"
    if "s/390" in out or "s390" in out:
        return "s390x"
    if "powerpc" in out or "ppc" in out:
        return "ppc64"
    return "unknown"


# A row of the macOS `sample` Call graph section, post-parse. `depth` is
# the unified tree depth (indent + 1 if the line was a '+' continuation
# of its parent). `pcs` is the list of integer PCs from the bracketed
# trailer; leader lines have one PC, '+' lines have a comma list.
Frame = namedtuple("Frame", ["depth", "count", "symbol", "binary", "pcs"])

# The marker frame the workload wraps run_main_graph in. Any stack that
# passes through this symbol is counted as "inside the model".
INSIDE_MARKER = "invoke_run"

# Embedded inference-workload .cpp. The user supplies a separate .cpp
# defining `extern "C" OMTensorList *initialize_model_input(void);`;
# this file provides everything else (main, argv parsing, the noinline
# invoke_run wrapper that survives tail-call optimization, the timing
# loop). The driver writes this string to a temp file and compiles it
# alongside the user's init .cpp.
WORKLOAD_CPP = r"""// Auto-generated by profile-model.py.
// The user's companion .cpp must define:
//   extern "C" OMTensorList *initialize_model_input(void);

// macOS deprecated ucontext_t and gates the header behind
// _XOPEN_SOURCE; define it before any system header so the include
// of ucontext.h below succeeds. _XOPEN_SOURCE on its own also strips
// Darwin extensions (Dl_info from dlfcn.h, etc.), so re-enable those
// with _DARWIN_C_SOURCE. No-ops on Linux.
#if defined(__APPLE__)
#  if !defined(_XOPEN_SOURCE)
#    define _XOPEN_SOURCE 700
#  endif
#  if !defined(_DARWIN_C_SOURCE)
#    define _DARWIN_C_SOURCE
#  endif
#endif

#include <chrono>
#include <cstdint>
#include <cstdio>
#include <cstdlib>
#include <iostream>
#include <string>

#include <atomic>
#include <signal.h>
#include <sys/time.h>
#include <dlfcn.h>
#include <ucontext.h>

#include <OnnxMlirRuntime.h>

extern "C" OMTensorList *run_main_graph(OMTensorList *);
extern "C" OMTensorList *initialize_model_input(void);

// In-process SIGPROF sampler ------------------------------------------
// When the env var PROFILE_INPROC_DUMP is set at startup, install a
// SIGPROF handler driven by a 1 ms ITIMER_PROF; record the leaf PC of
// every interrupt that fires while in_model_flag != 0. invoke_run
// flips that flag around its call to run_main_graph so only model
// inference time is sampled. Pure user-space — no perf_event_open, no
// kernel privilege — works in any container even when perf is
// unavailable. The dump is a binary header + uint64 PC array consumed
// by profile-model.py's _parse_inproc_dump.
namespace inproc_sampler {

static std::atomic<int> in_model_flag{0};
static std::atomic<size_t> sample_count{0};
// 100M samples = 800 MB cap; ~28 hours of 1 ms sampling. Overflow is
// silently dropped so we never grow the buffer mid-handler.
static constexpr size_t kMaxSamples = 100ull * 1000 * 1000;
static uint64_t *sample_buffer = nullptr;
static const char *dump_path = nullptr;

static void handler(int, siginfo_t *, void *ctx_) {
  if (!in_model_flag.load(std::memory_order_relaxed)) return;
  ucontext_t *ctx = reinterpret_cast<ucontext_t *>(ctx_);
  uint64_t pc;
#if defined(__APPLE__) && defined(__aarch64__)
  pc = (uint64_t)ctx->uc_mcontext->__ss.__pc;
#elif defined(__APPLE__) && defined(__x86_64__)
  pc = (uint64_t)ctx->uc_mcontext->__ss.__rip;
#elif defined(__linux__) && defined(__aarch64__)
  pc = (uint64_t)ctx->uc_mcontext.pc;
#elif defined(__linux__) && defined(__x86_64__)
  pc = (uint64_t)ctx->uc_mcontext.gregs[REG_RIP];
#elif defined(__linux__) && defined(__s390x__)
  // The Program Status Word's addr field holds the current
  // instruction address on z/Architecture. For 64-bit user code the
  // high bits aren't mode flags so we can use the value as-is.
  pc = (uint64_t)ctx->uc_mcontext.psw.addr;
#elif defined(__linux__) && defined(__powerpc64__)
  // ppc64 Linux: NIP (next instruction pointer) is the equivalent of
  // RIP/PC; on ELFv2 it's the regular PC at sample time.
  pc = (uint64_t)ctx->uc_mcontext.gp_regs[32];  // PT_NIP
#else
#warning "in-process sampler: unknown architecture; PCs will be 0"
  pc = 0;
#endif
  size_t i = sample_count.fetch_add(1, std::memory_order_relaxed);
  if (i < kMaxSamples) sample_buffer[i] = pc;
}

static inline void mark_in_model(int v) {
  in_model_flag.store(v, std::memory_order_relaxed);
}

static bool init() {
  dump_path = std::getenv("PROFILE_INPROC_DUMP");
  if (!dump_path) return false;  // sampler off
  sample_buffer = new (std::nothrow) uint64_t[kMaxSamples];
  if (!sample_buffer) {
    std::cerr << "[inproc-sampler] failed to allocate "
              << (kMaxSamples * sizeof(uint64_t)) << " bytes\n";
    return false;
  }
  struct sigaction sa = {};
  sa.sa_sigaction = handler;
  sa.sa_flags = SA_SIGINFO | SA_RESTART;
  sigemptyset(&sa.sa_mask);
  if (sigaction(SIGPROF, &sa, nullptr) != 0) {
    perror("[inproc-sampler] sigaction");
    return false;
  }
  // 1 ms periodic ITIMER_PROF: fires only while the process is using
  // CPU (vs ITIMER_REAL which fires on wall-clock), so every sample
  // catches the process actually running.
  struct itimerval tv = {};
  tv.it_interval.tv_usec = 1000;
  tv.it_value.tv_usec = 1000;
  if (setitimer(ITIMER_PROF, &tv, nullptr) != 0) {
    perror("[inproc-sampler] setitimer");
    return false;
  }
  std::cout << "[inproc-sampler] enabled, dump=" << dump_path << std::endl;
  return true;
}

// Write `v` to `fp` as 8 little-endian bytes, regardless of host
// endianness. We can't `fwrite(&v, 8, 1, fp)` because that emits the
// host byte order — and the python parser is hard-coded to LE — so on
// big-endian hosts (s390x, ppc64be) the bytes would land reversed
// relative to what the parser expects.
static void fwrite_le64(uint64_t v, std::FILE *fp) {
  unsigned char b[8];
  b[0] = (unsigned char)(v & 0xff);
  b[1] = (unsigned char)((v >> 8) & 0xff);
  b[2] = (unsigned char)((v >> 16) & 0xff);
  b[3] = (unsigned char)((v >> 24) & 0xff);
  b[4] = (unsigned char)((v >> 32) & 0xff);
  b[5] = (unsigned char)((v >> 40) & 0xff);
  b[6] = (unsigned char)((v >> 48) & 0xff);
  b[7] = (unsigned char)((v >> 56) & 0xff);
  std::fwrite(b, 1, 8, fp);
}

static void finalize() {
  if (!dump_path || !sample_buffer) return;
  // Stop the timer first so the buffer doesn't grow under us during
  // the dump write.
  struct itimerval off = {};
  setitimer(ITIMER_PROF, &off, nullptr);
  // Compute model.so's runtime base from a known symbol that lives in
  // it. dladdr returns the load address of the containing dso in
  // dli_fbase — same value extract_runtime_base recovers from
  // /proc/<pid>/maps or `sample`'s Binary Images section.
  Dl_info info = {};
  uint64_t runtime_base = 0;
  if (dladdr(reinterpret_cast<void *>(&run_main_graph), &info)) {
    runtime_base = reinterpret_cast<uint64_t>(info.dli_fbase);
  }
  size_t n = sample_count.load();
  if (n > kMaxSamples) n = kMaxSamples;
  std::FILE *fp = std::fopen(dump_path, "wb");
  if (!fp) {
    perror("[inproc-sampler] fopen");
    return;
  }
  // Header: magic (8B) + runtime_base (8B) + n_samples (8B) +
  // reserved (8B). All written little-endian so the layout is host-
  // endian-agnostic. If you ever change the layout, bump the trailing
  // two chars of "PMSAMP01" and update INPROC_MAGIC on the python
  // side to match.
  fwrite_le64(0x3130504D41534D50ULL, fp);  // magic = "PMSAMP01"
  fwrite_le64(runtime_base, fp);
  fwrite_le64((uint64_t)n, fp);
  fwrite_le64(0, fp);                       // reserved
  for (size_t i = 0; i < n; ++i) {
    fwrite_le64(sample_buffer[i], fp);
  }
  std::fclose(fp);
  std::cout << "[inproc-sampler] wrote " << n << " samples to "
            << dump_path << std::endl;
}

}  // namespace inproc_sampler

// Thin wrapper around run_main_graph. Two roles:
//   1. Be a single, easy-to-grep stack frame so stack-based samplers
//      (macOS sample / Linux perf) can classify "inside the model"
//      via ancestor lookup. noinline alone is not enough — clang -O2
//      tail-call-optimizes `return run_main_graph(input)` into a
//      direct jump and the frame disappears. The asm volatile barrier
//      forces `r` to live past the call, demanding a real return path
//      and thus a real stack frame.
//   2. Flip the in_model flag the in-process SIGPROF sampler keys off,
//      so when --sampler inproc is in use only PCs taken during
//      run_main_graph land in the dump.
extern "C" __attribute__((noinline)) OMTensorList *
invoke_run(OMTensorList *input) {
  inproc_sampler::mark_in_model(1);
  OMTensorList *r = run_main_graph(input);
  asm volatile("" : : "r"(r) : "memory");
  inproc_sampler::mark_in_model(0);
  return r;
}

int main(int argc, char *argv[]) {
  // Tiny hand-rolled flag parser. Accepted forms:
  //   (no args)
  //   -t 30
  //   --time 30
  int duration = 40;
  for (int i = 1; i < argc; ++i) {
    const std::string a = argv[i];
    if ((a == "-t" || a == "--time") && i + 1 < argc) {
      duration = std::atoi(argv[++i]);
    } else {
      std::cerr << "Usage: " << argv[0] << " [-t|--time <seconds>]\n";
      return 1;
    }
  }
  if (duration <= 0) {
    std::cerr << "Usage: " << argv[0] << " [-t|--time <seconds>]\n";
    return 1;
  }

  // No-op when PROFILE_INPROC_DUMP is unset; arms the SIGPROF sampler
  // when it's set. Done before the input setup so init-side allocs
  // (initialize_model_input + the model's own setup) don't show up
  // as samples — only invoke_run-bracketed work counts.
  inproc_sampler::init();

  OMTensorList *input = initialize_model_input();
  if (!input) {
    std::cerr << "initialize_model_input() returned null\n";
    return 2;
  }

  const auto start = std::chrono::steady_clock::now();
  int iteration = 0;

  while (true) {
    const auto now = std::chrono::steady_clock::now();
    const double elapsed = std::chrono::duration<double>(now - start).count();
    if (elapsed >= duration)
      break;

    OMTensorList *outputList = invoke_run(input);
    if (!outputList) {
      std::cerr << "invoke_run returned null\n";
      omTensorListDestroy(input);
      return 5;
    }

    ++iteration;
    if (iteration % 50 == 0) {
      std::cout << "Iteration " << iteration << "  (" << elapsed << "s / "
                << duration << "s)" << std::endl;
    }

    omTensorListDestroy(outputList);
  }

  omTensorListDestroy(input);

  const double total =
      std::chrono::duration<double>(std::chrono::steady_clock::now() - start)
          .count();
  std::cout << "Completed " << iteration << " iterations in " << total << "s"
            << std::endl;

  // Stops the timer and writes the PC dump if the sampler was armed.
  // No-op if PROFILE_INPROC_DUMP wasn't set at startup.
  inproc_sampler::finalize();
  return 0;
}
"""

