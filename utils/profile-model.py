INPROC_ENV_VAR = "PROFILE_INPROC_DUMP"
INPROC_MAGIC = 0x3230504D41534D50  # "PMSAMP02" little-endian
# Tag bits as set by the compiler / inspected by OMInstrument.c. Mirrors
# the InstrumentActions enum in OMCompilerRuntimeTypes.h (bit 0 =
# before, bit 1 = after). Used by attribute_offsets_to_ops to flag the
# brief samples that fall after an op finished — those go to
# `unattributed` instead of being credited to the just-finished op.
INPROC_TAG_BEFORE_BIT = 1 << 0
INPROC_TAG_AFTER_BIT = 1 << 1


def _resolve_sampler(name):
    """Map `auto` to `inproc` on every platform. inproc is the default
    because it produces the cleanest per-PC histogram (the kernel-side
    `sample`/`perf` paths aggregate sibling PCs into call-tree rows,
    which our parser must redistribute uniformly — that adds artificial
    blur to the instruction-mix output). The kernel-side paths are
    still useful when you need the per-binary leaf / top-frame tables,
    which require captured call stacks.
    """
    if name == "auto":
        return "inproc"
    return name


def _parse_inproc_dump(path):
    """Read the binary dump written by the embedded SIGPROF sampler.

    Returns (total_samples, runtime_base, offset_counts, ordered_samples)
    where:
      - offset_counts is a {offset_in_so: count} histogram (used by
        order-insensitive consumers like the instruction-mix table)
      - ordered_samples is the list of per-sample dicts in firing
        order. Each entry has keys `offset` (PC minus runtime_base),
        `op_name` / `node_name` (resolved strings — None when the
        OMInstrument marker globals were unset, e.g. before any op
        ran), and `tag` (the InstrumentActions bits). Consumed by
        `attribute_offsets_to_ops`, which uses `op_name` directly to
        attribute samples to ops — bypassing both spatial DWARF span
        lookup and the temporal "last in-span" heuristic. The
        marker-based path is exact for callee-resident PCs (e.g.
        zDNN's `invoke_nnpa` for s390x NNPA dispatches), where the
        leaf PC lives outside main_graph and no DWARF span can ever
        cover it.

    Layout (little-endian, v2 magic = "PMSAMP02"):
        u64 magic
        u64 runtime_base
        u64 n_samples
        u64 n_strings
        struct { u64 pc; u64 op_ptr; u64 node_ptr; u64 tag; }
            samples[n_samples]
        struct { u64 ptr; u64 len; u8 bytes[len]; u8 pad[(8-len%8)%8]; }
            strings[n_strings]
    """
    with open(path, "rb") as f:
        data = f.read()
    if len(data) < 32:
        sys.exit(f"Error: inproc dump truncated: {path}")
    magic, runtime_base, n_samples, n_strings = struct.unpack("<QQQQ", data[:32])
    if magic != INPROC_MAGIC:
        sys.exit(
            f"Error: bad magic in {path} "
            f"(got 0x{magic:016x}, want 0x{INPROC_MAGIC:016x}). "
            "Stale dump from an older workload version — recompile with "
            "the current profile-model.py and try again."
        )
    pos = 32
    sample_bytes = n_samples * 32
    if len(data) < pos + sample_bytes:
        sys.exit(f"Error: inproc dump truncated sample array: {path}")
    raw = struct.unpack_from(f"<{n_samples * 4}Q", data, pos)
    pos += sample_bytes

    # Pull the string table out next so we can resolve op_ptr/node_ptr
    # while building the per-sample dicts.
    str_table = {}
    for _ in range(n_strings):
        if len(data) < pos + 16:
            sys.exit(f"Error: inproc dump truncated string-table header: {path}")
        ptr, length = struct.unpack_from("<QQ", data, pos)
        pos += 16
        if len(data) < pos + length:
            sys.exit(f"Error: inproc dump truncated string body: {path}")
        try:
            str_table[ptr] = data[pos : pos + length].decode("utf-8", errors="replace")
        except Exception:
            str_table[ptr] = ""
        # Skip the length bytes plus 0..7 zero-pad bytes that align the
        # next entry to an 8-byte boundary.
        pos += length + ((8 - length % 8) % 8)

    ordered_samples = []
    counts = {}
    for i in range(n_samples):
        pc, op_ptr, node_ptr, tag = raw[i * 4 : i * 4 + 4]
        offset = pc - runtime_base
        counts[offset] = counts.get(offset, 0) + 1
        ordered_samples.append(
            {
                "offset": offset,
                "op_name": str_table.get(op_ptr) if op_ptr else None,
                "node_name": str_table.get(node_ptr) if node_ptr else None,
                "tag": tag,
            }
        )
    return n_samples, runtime_base, counts, ordered_samples


def _spawn_workload(binary, model_so, duration):
    """Launch the workload binary, return (proc, stdout_f, stderr_f)
    or (None, None, None) if it exited before sampling started.

    Pads the workload's `-t` by 10 s so the loop is still running when
    the sampler finishes; otherwise the last sampled second can land
    after exit and miss frames.

    Models compiled with --instrument-onnx-ops emit a per-op SIG-REPORT
    line on every inference call; for a 30 s loop that's millions of
    writes blocking the workload on stdout I/O instead of the math we're
    trying to profile. ONNX_MLIR_NO_INSTRUMENT=1 silences it at runtime
    without recompiling.

    A statically-linked model.so may have an install_name / SONAME
    that's just the basename; dyld/ld.so won't find it unless the
    directory is on the loader's search path. Prepend onto whichever
    env var the loader uses (DYLD_LIBRARY_PATH / LD_LIBRARY_PATH) so
    the user's existing value still wins for everything else.
    """
    cmd = [binary, "-t", str(duration + 10)]
    out_f = open(WORKLOAD_STDOUT_LOG, "w")
    err_f = open(WORKLOAD_STDERR_LOG, "w")
    env = os.environ.copy()
    env["ONNX_MLIR_NO_INSTRUMENT"] = "1"
    so_dir = os.path.dirname(os.path.abspath(model_so))
    existing = env.get(DYLIB_PATH_ENV, "")
    env[DYLIB_PATH_ENV] = so_dir + (":" + existing if existing else "")
    print(f"Launching: {' '.join(cmd)}")
    print("  env: ONNX_MLIR_NO_INSTRUMENT=1")
    print(f"  env: {DYLIB_PATH_ENV}={env[DYLIB_PATH_ENV]}")
    proc = subprocess.Popen(cmd, stdout=out_f, stderr=err_f, env=env)
    print(f"PID: {proc.pid}")

    # Give the process a moment to start (loader resolves model.so,
    # init buffers) so sampling starts mid-loop rather than during init.
    time.sleep(2)
    if proc.poll() is not None:
        out_f.close()
        err_f.close()
        print("Workload exited immediately:")
        for label, p in (
            ("stdout", WORKLOAD_STDOUT_LOG),
            ("stderr", WORKLOAD_STDERR_LOG),
        ):
            try:
                with open(p) as f:
                    tail = f.read()[-4000:]
                if tail:
                    print(f"--- {label} (tail) ---")
                    print(tail)
            except OSError:
                pass
        return None, None, None
    return proc, out_f, err_f


def _wait_workload(proc, out_f, err_f):
    try:
        proc.wait(timeout=5)
    except subprocess.TimeoutExpired:
        proc.terminate()
        proc.wait()
    out_f.close()
    err_f.close()


def run_and_sample(binary, model_so, duration, sampler="auto"):
    """Spawn the binary, sample for `duration` seconds, return the
    path to the sampler's output file (or None on failure).

    `sampler` selects the backend:
      - 'sample'  : macOS Apple `sample`
      - 'perf'    : Linux `perf record` + `perf script`
      - 'inproc'  : embedded SIGPROF sampler in the workload itself
                    (no kernel privilege needed; works in restricted
                    containers where perf_event_open is blocked)
      - 'auto'    : `sample` on macOS, `perf` on Linux
    """
    sampler = _resolve_sampler(sampler)
    if sampler == "sample":
        if not IS_MAC:
            sys.exit("Error: --sampler sample is macOS-only.")
        return _run_and_sample_macos(binary, model_so, duration)
    if sampler == "perf":
        if not IS_LINUX:
            sys.exit("Error: --sampler perf is Linux-only.")
        return _run_and_sample_linux(binary, model_so, duration)
    if sampler == "inproc":
        return _run_and_sample_inproc(binary, model_so, duration)
    sys.exit(f"Error: unknown sampler {sampler!r}.")


def _run_and_sample_inproc(binary, model_so, duration):
    """Drive the embedded SIGPROF sampler. We just spawn the workload
    with PROFILE_INPROC_DUMP set in its env; the workload installs its
    own SIGPROF handler on startup and writes the dump on exit. No
    external profiler is attached, so no caps/sysctls/seccomp involved.
    """
    # Remove any stale dump from a previous run so a workload crash
    # before the new write doesn't make us read the old data back.
    try:
        os.unlink(INPROC_DUMP_PATH)
    except FileNotFoundError:
        pass

    out_f = open(WORKLOAD_STDOUT_LOG, "w")
    err_f = open(WORKLOAD_STDERR_LOG, "w")
    env = os.environ.copy()
    env["ONNX_MLIR_NO_INSTRUMENT"] = "1"
    env[INPROC_ENV_VAR] = INPROC_DUMP_PATH
    so_dir = os.path.dirname(os.path.abspath(model_so))
    existing = env.get(DYLIB_PATH_ENV, "")
    env[DYLIB_PATH_ENV] = so_dir + (":" + existing if existing else "")

    # No padding (vs +10 for sample/perf): with inproc the workload
    # IS the sampler, so its `-t` is exactly the duration we want.
    cmd = [binary, "-t", str(duration)]
    print(f"Launching: {' '.join(cmd)}")
    print("  env: ONNX_MLIR_NO_INSTRUMENT=1")
    print(f"  env: {INPROC_ENV_VAR}={INPROC_DUMP_PATH}")
    print(f"  env: {DYLIB_PATH_ENV}={env[DYLIB_PATH_ENV]}")
    proc = subprocess.Popen(cmd, stdout=out_f, stderr=err_f, env=env)
    print(f"PID: {proc.pid}")
    print(f"Sampling in-process for {duration}s ...")
    try:
        # Generous timeout — the workload runs for `duration` plus
        # init / finalize overhead. 30 s buffer covers slow init on
        # cold caches.
        proc.wait(timeout=duration + 30)
    except subprocess.TimeoutExpired:
        print("Workload exceeded timeout; terminating.")
        proc.terminate()
        proc.wait()
    out_f.close()
    err_f.close()

    if not os.path.exists(INPROC_DUMP_PATH):
        print("Error: in-process sampler produced no dump file.")
        for label, p in (
            ("stdout", WORKLOAD_STDOUT_LOG),
            ("stderr", WORKLOAD_STDERR_LOG),
        ):
            try:
                with open(p) as f:
                    tail = f.read()[-4000:]
                if tail:
                    print(f"--- {label} (tail) ---")
                    print(tail)
            except OSError:
                pass
        return None
    return INPROC_DUMP_PATH


def _run_and_sample_macos(binary, model_so, duration):
    proc, out_f, err_f = _spawn_workload(binary, model_so, duration)
    if proc is None:
        return None
    print(f"Sampling for {duration}s -> {SAMPLE_FILE_PATH}")
    try:
        subprocess.run(
            ["sample", str(proc.pid), str(duration), "-f", SAMPLE_FILE_PATH],
            timeout=duration + 10,
            capture_output=True,
        )
    except subprocess.TimeoutExpired:
        pass
    _wait_workload(proc, out_f, err_f)
    if not os.path.exists(SAMPLE_FILE_PATH):
        print("Error: sample produced no output file.")
        return None
    return SAMPLE_FILE_PATH


def _run_and_sample_linux(binary, model_so, duration):
    """Linux equivalent of the macOS `sample` flow.

    Strategy: launch the workload, snapshot /proc/<pid>/maps so we can
    later resolve the .so's runtime base (the macOS path gets this from
    sample's `Binary Images:` block; Linux perf records it as MMAP
    events but reading it directly from /proc is simpler), then run
    `perf record` against the live PID and convert the result to text
    with `perf script`.
    """
    proc, out_f, err_f = _spawn_workload(binary, model_so, duration)
    if proc is None:
        return None

    # Snapshot /proc/<pid>/maps now, while the process is still
    # running — once it exits, the entry is gone. Used later by
    # _extract_runtime_base_linux to find the .so's load address.
    try:
        with open(f"/proc/{proc.pid}/maps") as fm:
            maps_content = fm.read()
        with open(LINUX_MAPS_PATH, "w") as fout:
            fout.write(maps_content)
    except OSError as e:
        print(f"Warning: could not read /proc/{proc.pid}/maps: {e}")

    print(f"Sampling for {duration}s with `perf record` -> " f"{LINUX_PERF_DATA_PATH}")
    perf_record = [
        "perf",
        "record",
        "-F",
        "1000",
        "-g",
        "-p",
        str(proc.pid),
        "-o",
        LINUX_PERF_DATA_PATH,
        "--",
        "sleep",
        str(duration),
    ]
    try:
        r = subprocess.run(
            perf_record, timeout=duration + 30, capture_output=True, text=True
        )
        if r.returncode != 0:
            print("--- perf record stderr ---")
            print(r.stderr)
            _wait_workload(proc, out_f, err_f)
            return None
    except FileNotFoundError:
        _wait_workload(proc, out_f, err_f)
        sys.exit(
            "Error: `perf` not found. Install linux-perf "
            "(Ubuntu: `sudo apt install linux-tools-common "
            "linux-tools-$(uname -r)`)."
        )
    except subprocess.TimeoutExpired:
        pass

    # Resolve the recorded data into a textual form we can parse.
    print(f"Converting via `perf script` -> {SAMPLE_FILE_PATH}")
    with open(SAMPLE_FILE_PATH, "w") as fout:
        r = subprocess.run(
            ["perf", "script", "-i", LINUX_PERF_DATA_PATH],
            stdout=fout,
            stderr=subprocess.PIPE,
            text=True,
        )
    if r.returncode != 0:
        print("--- perf script stderr ---")
        print(r.stderr)
        _wait_workload(proc, out_f, err_f)
        return None

    _wait_workload(proc, out_f, err_f)
    if not os.path.exists(SAMPLE_FILE_PATH) or os.path.getsize(SAMPLE_FILE_PATH) == 0:
        print("Error: perf script produced no output.")
        return None
    return SAMPLE_FILE_PATH


# A line in the call graph looks like one of:
#   "<indent><count> <symbol>  (in <binary>) + <off>  [0xPC]"      (leader)
#   "<indent>+ <count> <symbol>  (in <binary>) ..."                (one deeper)
#   "<indent>+   <count> <symbol>  (in <binary>) ..."              (two deeper)
#   "<indent>+   ! <count> <symbol>  (in <binary>) ..."            (collapsed)
# macOS `sample` encodes depth via the column position of the count
# digit, NOT just the leading indent: extra spaces after a '+' marker,
# and the optional '!' collapse marker, also push the count column
# rightward. We capture all the prefix material (indent / '+'/spaces /
# '!'/space) so depth = m.start('count') correctly reflects the tree
# level — using just the indent length collapses every '+' line to the
# same depth regardless of its actual logical depth.
_CG_LINE_RE = re.compile(
    r"^(?P<prefix>\s*(?:\+\s+)?(?:!\s+)?)"
    r"(?P<count>\d+)\s+(?P<symbol>.+?)"
    r"\s+\(in (?P<binary>[^)]+)\)"
    r"(?:[^\[]*\[(?P<pcs>[^\]]+)\])?",
    re.IGNORECASE,
)

_THREAD_RE = re.compile(r"^\s*(\d+)\s+Thread_\d+")


def _parse_pc_list(s):
    """Pull integer PCs out of a `[0x...,0x...,...]` trailer.

    `sample` collapses sibling PCs into a comma list and may end with a
    literal "..." truncation token; we drop that and any non-`0x...`
    junk. Returns (pc_list, truncated_flag).
    """
    if not s:
        return [], False
    raw = [p.strip() for p in s.split(",")]
    truncated = any(p.startswith(".") for p in raw)
    pcs = [int(p, 16) for p in raw if p.startswith("0x")]
    return pcs, truncated


def parse_call_graph(sample_file):
    """Return a list of Frame namedtuples in document/DFS order.

    On macOS the input is `sample`'s pre-aggregated call-graph tree
    text. On Linux it's `perf script`'s flat list of (one-stack-per-
    block) samples that we aggregate into the same Frame shape so all
    downstream extractors stay platform-agnostic.
    """
    return (_parse_call_graph_macos if IS_MAC else _parse_call_graph_linux)(sample_file)


def _parse_call_graph_macos(sample_file):
    """Parse macOS `sample`'s Call graph: section into Frame list.
    Depth combines indentation with the +-continuation marker so that
    '+' children sit one level deeper than the leader they follow.
    """
    parsed = []
    in_call_graph = False
    with open(sample_file) as f:
        for line in f:
            if line.startswith("Call graph:"):
                in_call_graph = True
                continue
            if in_call_graph and (
                line.startswith("Total number in stack")
                or line.startswith("Binary Images:")
            ):
                break
            if not in_call_graph:
                continue
            m = _CG_LINE_RE.match(line)
            if not m:
                continue
            depth = m.start("count")
            pcs, _ = _parse_pc_list(m.group("pcs"))
            parsed.append(
                Frame(
                    depth=depth,
                    count=int(m.group("count")),
                    symbol=m.group("symbol").strip(),
                    binary=m.group("binary").strip(),
                    pcs=pcs,
                )
            )
    return parsed


# perf script frame line: leading whitespace, hex PC, symbol, "(binary)".
# Examples we need to handle:
#   "    7f1234abcd main_graph_foo+0x10 (/path/to/model.so)"
#   "          ffffffff [unknown] ([kernel.kallsyms])"
# The symbol part may include "+0x..." offset which we strip; binary is
# the path inside the trailing parentheses (we keep just the basename
# so it matches what extract_runtime_base returns).
_PERF_FRAME_RE = re.compile(r"^\s+([0-9a-f]+)\s+(.+?)\s+\((.+?)\)\s*$", re.IGNORECASE)
# Header lines ("binary 12345 [001] 1234.567890: cpu-clock:") are
# implicitly skipped — they don't match _PERF_FRAME_RE — so we don't
# need to recognize them; only blank lines separate sample blocks.


def _parse_call_graph_linux(sample_file):
    """Parse `perf script` output into a Frame list compatible with the
    macOS path. Each blank-line-separated block is one stack sample;
    frames inside are leaf-first (deepest first). We build a virtual
    call tree by inserting each stack root-to-leaf and accumulating
    counts at each unique (symbol, binary) path; a DFS over the result
    yields the Frame list extractors expect.
    """
    stacks = _read_perf_stacks(sample_file)
    return _stacks_to_frames(stacks)


def _read_perf_stacks(sample_file):
    """Yield (weight, [(pc, symbol, binary)]) tuples — one per stack
    block in `perf script` output. Weight is 1 per sample at the
    default `-F 1000` rate; if the user re-records with a different
    frequency the weights still stay at 1, which is the right thing
    (each block IS one sample regardless of the recording rate).
    """
    stacks = []
    current = []  # list of (pc, symbol, binary), leaf first
    with open(sample_file) as f:
        for line in f:
            if not line.strip():
                if current:
                    stacks.append((1, current))
                    current = []
                continue
            m = _PERF_FRAME_RE.match(line)
            if m:
                pc = int(m.group(1), 16)
                sym = m.group(2).strip()
                # Strip "+0x..." offset suffix that perf script always
                # appends. Keep the bare symbol so the inside-marker
                # match (`symbol == "invoke_run"`) works.
                sym = re.sub(r"\+0x[0-9a-f]+$", "", sym)
                binary = os.path.basename(m.group(3).strip())
                current.append((pc, sym, binary))
            # Header lines (and anything else) are skipped silently;
            # they only matter for separating blocks via the blank
            # lines that follow each one.
    if current:
        stacks.append((1, current))
    return stacks


def _stacks_to_frames(stacks):
    """Aggregate a flat list of (weight, [frames]) into a virtual
    call tree, then DFS-emit Frame namedtuples in document order.
    Depth is the position from the root (1 = outermost frame); the
    extractors only require monotonic-on-descend depth.
    """
    # Each tree node: dict with 'count', 'pcs' (list — duplicates fine,
    # _parse_pc_list-style consumers de-dup), 'children'
    # ((symbol, binary) -> child node), 'symbol', 'binary'.
    root = {
        "count": 0,
        "pcs": [],
        "children": {},
        "symbol": "<root>",
        "binary": "<root>",
    }
    for weight, frames in stacks:
        # frames are leaf-first; reverse for root-first descent.
        node = root
        for pc, sym, bin_ in reversed(frames):
            key = (sym, bin_)
            child = node["children"].get(key)
            if child is None:
                child = {
                    "count": 0,
                    "pcs": [],
                    "children": {},
                    "symbol": sym,
                    "binary": bin_,
                }
                node["children"][key] = child
            child["count"] += weight
            child["pcs"].append(pc)
            node = child

    out = []

    def visit(node, depth):
        if depth > 0:
            # De-dup PCs so a hot frame doesn't blow up the per-PC
            # weight when the same address recurred in many samples
            # (the offset_counts walk distributes the frame weight
            # evenly across visible PCs, so duplicates would skew it).
            unique_pcs = sorted(set(node["pcs"]))
            out.append(
                Frame(
                    depth=depth,
                    count=node["count"],
                    symbol=node["symbol"],
                    binary=node["binary"],
                    pcs=unique_pcs,
                )
            )
        for child in node["children"].values():
            visit(child, depth + 1)

    visit(root, 0)
    return out


def extract_total_sample_count(sample_file):
    """Total samples across the captured run.

    On macOS this sums the per-thread counts that `sample` prints at
    the top of each thread's call graph. On Linux it counts the number
    of stack blocks in `perf script` output (each block is one sample).
    """
    return (
        _extract_total_sample_count_macos
        if IS_MAC
        else _extract_total_sample_count_linux
    )(sample_file)


def _extract_total_sample_count_macos(sample_file):
    total = 0
    with open(sample_file) as f:
        for line in f:
            m = _THREAD_RE.match(line)
            if m:
                total += int(m.group(1))
    return total


def _extract_total_sample_count_linux(sample_file):
    # Each non-empty run of frame lines, terminated by a blank line, is
    # one sample. Count transitions from in-block to out-of-block.
    count = 0
    in_block = False
    with open(sample_file) as f:
        for line in f:
            if line.strip():
                in_block = True
            elif in_block:
                count += 1
                in_block = False
    if in_block:
        count += 1
    return count


def extract_inside_marker_count(parsed, marker=INSIDE_MARKER):
    """Count samples whose stack contains a frame named `marker`.

    We walk the call graph as a tree (via the depth column from
    parse_call_graph). Every time we descend into a frame whose symbol
    matches the marker AND whose ancestors did NOT already match, we add
    that frame's count to the total — that's the entry-point count of
    samples passing through the marker for the first time. Subsequent
    expansions of the same samples (deeper '+' frames) are skipped via
    the in-marker-subtree flag, which prevents double counting.
    """
    total = 0
    # Stack of (depth, in_marker_subtree_already)
    stack = []
    for f in parsed:
        while stack and stack[-1][0] >= f.depth:
            stack.pop()
        parent_in = stack[-1][1] if stack else False
        is_marker = f.symbol == marker
        in_subtree = parent_in or is_marker
        if is_marker and not parent_in:
            total += f.count
        stack.append((f.depth, in_subtree))
    return total


def extract_per_binary_leaf_counts(parsed):
    """Per-binary count of leaf call-tree frames.

    Each sample produces one stack ending at one leaf PC; that leaf
    carries the sample's count. Internal nodes carry the same count
    cumulatively, so summing everything double-counts. To get a clean
    per-binary breakdown that sums to roughly total_samples, attribute
    each frame's "leaf samples" — count minus the sum of its children's
    counts — to its binary.
    """
    counts = {}
    # frames: list of [depth, count, binary, children_sum]
    stack = []

    def close(frame):
        _, fcount, fbinary, fcsum = frame
        leaf = fcount - fcsum
        if leaf > 0:
            counts[fbinary] = counts.get(fbinary, 0) + leaf

    for f in parsed:
        while stack and stack[-1][0] >= f.depth:
            close(stack.pop())
        if stack:
            stack[-1][3] += f.count
        stack.append([f.depth, f.count, f.binary, 0])
    while stack:
        close(stack.pop())
    return counts


def extract_top_inside_symbols(parsed, marker=INSIDE_MARKER, limit=20):
    """Top-N (symbol, binary) leaves under the marker by leaf samples.

    Same leaf logic as extract_per_binary_leaf_counts, but keyed by
    (symbol, binary) and only counting frames that sit inside the
    marker's subtree. Useful for at-a-glance "where in the model is
    time spent" without disassembling the .so.
    """
    counts = {}
    stack = []  # [depth, count, symbol, binary, children_sum, in_subtree]

    def close(frame):
        _depth, fcount, fsymbol, fbinary, fcsum, fin = frame
        if not fin:
            return
        leaf = fcount - fcsum
        if leaf > 0:
            key = (fsymbol, fbinary)
            counts[key] = counts.get(key, 0) + leaf

    for f in parsed:
        while stack and stack[-1][0] >= f.depth:
            close(stack.pop())
        parent_in = stack[-1][5] if stack else False
        in_subtree = parent_in or (f.symbol == marker)
        if stack:
            stack[-1][4] += f.count
        stack.append([f.depth, f.count, f.symbol, f.binary, 0, in_subtree])
    while stack:
        close(stack.pop())

    items = sorted(counts.items(), key=lambda kv: -kv[1])
    return items[:limit]


def extract_runtime_base(sample_file, so_name):
    """Runtime base address of the .so during the sampled run.

    Subtracting this from a sampled PC gives the in-image offset, which
    is what objdump addresses are keyed on (modulo __TEXT vmaddr — see
    get_load_address). Returns 0 if not found.
    """
    return (_extract_runtime_base_macos if IS_MAC else _extract_runtime_base_linux)(
        sample_file, so_name
    )


def _extract_runtime_base_macos(sample_file, so_name):
    """Parse macOS sample's `Binary Images:` block, e.g.:
    "0x300000000 - 0x342... +model.so <UUID> /path/to/model.so"
    """
    with open(sample_file) as f:
        content = f.read()
    base_re = re.compile(
        rf"0x([0-9a-f]+)\s+-\s+0x[0-9a-f]+\s+\+?{re.escape(so_name)}\b", re.IGNORECASE
    )
    m = base_re.search(content)
    if not m:
        return 0
    return int(m.group(1), 16)


def _extract_runtime_base_linux(sample_file, so_name):
    """Parse the /proc/<pid>/maps snapshot captured at sample time.

    /proc/<pid>/maps lines look like:
        7f0c12345000-7f0c12def000 r-xp 00000000 fd:01 12345 /path/model.so
    A single .so produces several entries (one per ELF segment). The
    .text segment is `r-xp` (or `r-x-`); we pick the lowest start
    address with `x` in its permission bits and subtract the file
    offset, which gives the .so's image base — the same number macOS
    `sample` reports in its Binary Images section.
    """
    maps_path = sample_file + ".maps"
    if not os.path.exists(maps_path):
        # Fallback: also recognize a sidecar at the canonical path so
        # an older sample file paired with the latest run still works.
        if os.path.exists(LINUX_MAPS_PATH):
            maps_path = LINUX_MAPS_PATH
        else:
            return 0

    line_re = re.compile(
        r"^([0-9a-f]+)-[0-9a-f]+\s+(\S+)\s+([0-9a-f]+)\s+\S+\s+\S+\s+(\S+)$",
        re.IGNORECASE,
    )
    best = None
    try:
        with open(maps_path) as f:
            for line in f:
                m = line_re.match(line.strip())
                if not m:
                    continue
                perms = m.group(2)
                if "x" not in perms:
                    continue
                path = m.group(4)
                if os.path.basename(path) != so_name:
                    continue
                start = int(m.group(1), 16)
                file_off = int(m.group(3), 16)
                base = start - file_off
                if best is None or base < best:
                    best = base
    except OSError:
        return 0
    return best or 0


def extract_offset_counts_inside_marker(
    parsed, runtime_base, so_name, marker=INSIDE_MARKER
):
    """Per-PC counts within model.so, restricted to the marker subtree,
    plus a per-leaf-occurrence record set with ancestor chains.

    Walks the call graph as a tree, tracks "are we inside the marker's
    subtree", and for every line that (a) is in the subtree and (b)
    references model.so by name, distributes the line's weight uniformly
    across the bracketed PCs and accumulates by `pc - runtime_base`.

    Returns (counts, leaves) where:
      - counts: {offset_in_so -> total weight} — used for the
        instruction-mix histogram. Identical to the historic return.
      - leaves: list of {offset, count, ancestors} per leaf
        occurrence. `ancestors` is the deepest-first list of in-so
        ancestor offsets along this call-graph path. Used by
        `attribute_offsets_to_ops` to recover the calling ONNX op
        when the leaf PC sits in a callee that lives outside
        main_graph (e.g. zDNN's `invoke_nnpa` for s390x NNPA work):
        we walk the recorded stack until we land back inside a
        `__omip:` DWARF span.

    Continuation '+' lines collapse multiple sibling leaves into one row
    with a comma-list of PCs; we have no per-PC weight, so the line
    weight is split evenly. Truncated PC lists ('...') under-attribute
    weight to those leaves but the line total is preserved on the visible
    PCs (integer division remainder pinned to the first PC).
    """
    counts = {}
    leaves = []
    # Per-stack-entry tuple: (depth, in_marker_subtree, frame_offsets).
    # frame_offsets is the list of in-so PC offsets at this frame, or
    # [] if the frame doesn't reside in model.so. Storing it on the
    # stack lets us assemble each leaf's ancestor chain in one pass.
    stack = []
    for f in parsed:
        while stack and stack[-1][0] >= f.depth:
            stack.pop()
        parent_in = stack[-1][1] if stack else False
        in_subtree = parent_in or (f.symbol == marker)
        if f.binary == so_name and f.pcs:
            frame_offsets = [pc - runtime_base for pc in f.pcs]
        else:
            frame_offsets = []
        stack.append((f.depth, in_subtree, frame_offsets))
        if not in_subtree or not frame_offsets:
            continue
        n = len(f.pcs)
        base = f.count // n
        rem = f.count - base * n
        # Ancestor chain, deepest-first, restricted to in-so frames.
        # Self is excluded (stack[-1] is the current frame).
        ancestor_chain = []
        for ancestor in reversed(stack[:-1]):
            ancestor_chain.extend(ancestor[2])
        for i, offset in enumerate(frame_offsets):
            weight = base + (rem if i == 0 else 0)
            counts[offset] = counts.get(offset, 0) + weight
            leaves.append(
                {
                    "offset": offset,
                    "count": weight,
                    "ancestors": ancestor_chain,
                }
            )
    return counts, leaves


def get_load_address(so_path):
    """Address that objdump uses as the base for instruction addresses.

    Almost always 0 for dynamically-loaded libraries on both platforms,
    but we read it for correctness against any non-zero base.
    """
    return (_get_load_address_macos if IS_MAC else _get_load_address_linux)(so_path)


def _get_load_address_macos(so_path):
    """__TEXT segment vmaddr from `otool -l`."""
    r = subprocess.run(["otool", "-l", so_path], capture_output=True, text=True)
    if r.returncode != 0:
        return 0
    lines = r.stdout.split("\n")
    for i, line in enumerate(lines):
        if "LC_SEGMENT_64" in line and i + 1 < len(lines) and "__TEXT" in lines[i + 1]:
            for j in range(i, min(i + 10, len(lines))):
                m = re.search(r"vmaddr\s+0x([0-9a-f]+)", lines[j])
                if m:
                    return int(m.group(1), 16)
    return 0


def _get_load_address_linux(so_path):
    """First PT_LOAD's VirtAddr from `readelf -lW`. For a typical
    PIE/shared object this is 0; some toolchains emit a small base
    (e.g. 0x1000) so we don't hard-code zero.
    """
    r = subprocess.run(["readelf", "-lW", so_path], capture_output=True, text=True)
    if r.returncode != 0:
        return 0
    # readelf -l format (we only need the first LOAD entry):
    #   "  LOAD   0x000000 0x0000000000000000 0x... 0x... 0x... R E 0x10000"
    for line in r.stdout.splitlines():
        m = re.match(r"\s+LOAD\s+0x[0-9a-f]+\s+0x([0-9a-f]+)", line, re.IGNORECASE)
        if m:
            return int(m.group(1), 16)
    return 0


# objdump line layouts we need to handle:
#   macOS arm64 :  "     67c: 6d0133ed     \tstp\td13, d12, [sp, #0x10]"
#                  (one packed 8-hex-char encoding per instruction)
#   Linux arm64 :  "  680:\td10043ff \tsub\tsp, sp, #0x10"
#                  (same shape — a single 8-hex blob)
#   Linux x86_64:  "1234:\t48 89 e5             \tmov    %rsp,%rbp"
#                  (variable-length, space-separated 2-hex pairs)
#   Linux s390x :  "    1dc0:\teb 6f f0 30 00 24\tstmg\t%r6,%r15,48(%r15)"
#                  (variable-length, 2-hex pairs, tab between addr and bytes)
# The old "anything up to the first tab, then the mnemonic" pattern
# silently mis-parses the s390x layout (no separator between `:` and
# the bytes column → matches empty + grabs first byte as mnemonic).
# Skip the bytes column explicitly: either a single 2-8-char hex blob
# OR a sequence of 2-hex-pair groups with optional whitespace between.
# Mnemonic is anchored on a leading letter so a hex-only token can't
# masquerade as one (avoids `b.eq` confusion in arm conditional
# branches).
_INSTR_RE = re.compile(
    r"^\s*([0-9a-f]+):\s+"
    r"(?:[0-9a-f]{2,8}(?:\s+[0-9a-f]{2})*)\s+"
    r"([a-z][\w.]*)"
    r"(?:\s+([^\n]*))?",
    re.IGNORECASE,
)


def disassemble_so(so_path):
    """Run `objdump -d` on the .so and return {addr: (mnemonic, operands)}.

    Slow on big models (a few seconds for a 300 MB .so) — caller may
    want to cache the result. Mnemonics keep their .Nx /.Nb suffixes
    (e.g. `ldr.16b`, `fmla.4s`); the report aggregator strips them so
    the per-family histogram collapses size variants. Operands are kept
    raw including objdump's trailing `<symbol+off>` annotations.
    """
    print(f"Disassembling {so_path} ...")
    r = subprocess.run(["objdump", "-d", so_path], capture_output=True, text=True)
    if r.returncode != 0:
        print(f"Error: objdump failed: {r.stderr.strip()[:200]}")
        return {}
    instructions = {}
    for line in r.stdout.split("\n"):
        m = _INSTR_RE.match(line)
        if m:
            ops = m.group(3) or ""
            instructions[int(m.group(1), 16)] = (m.group(2), ops.rstrip())
    print(f"  disassembled {len(instructions)} instructions")
    return instructions


def map_offsets_to_mnemonics(offset_counts, instructions, load_addr):
    """Aggregate offset_counts by base instruction mnemonic.

    `instr_addr = offset + load_addr` is the address objdump printed.
    Mnemonics are aggregated by their base form (everything before the
    first '.'), so `ldr.4s` and `ldr.16b` both count under `ldr`. PCs
    that don't land on a known instruction address are skipped (this
    happens for e.g. data words inside a code section).

    Returns (mnem_counts, mapped_total, unmapped_total).
    """
    mnem_counts = {}
    mapped = 0
    unmapped = 0
    for offset, count in offset_counts.items():
        instr_addr = offset + load_addr
        entry = instructions.get(instr_addr)
        if entry is None:
            unmapped += count
            continue
        mnem = entry[0] if isinstance(entry, tuple) else entry
        base = mnem.split(".")[0]
        mnem_counts[base] = mnem_counts.get(base, 0) + count
        mapped += count
    return mnem_counts, mapped, unmapped


def write_annotated_disasm(so_path, offset_counts, load_addr, output_path):
    """Re-disassemble model.so via `objdump -d` and write a copy of
    the listing to `output_path`, appending `<<<=== X.XX%` to every
    instruction that received samples (percentage is over total mapped
    samples in offset_counts).

    Functions are sorted hottest-first and cold functions (zero
    samples) are omitted so the file stays scannable. Within each
    kept function the full body is preserved verbatim — register
    spills, jumps, and all — so you can see the local context around
    every hot line.
    """
    if not offset_counts:
        print(f"\nNot writing annotated disassembly: no sample data.")
        return
    print(f"\nGenerating annotated disassembly -> {output_path}")
    r = subprocess.run(["objdump", "-d", so_path], capture_output=True, text=True)
    if r.returncode != 0:
        print(f"  objdump failed: {r.stderr.strip()[:200]}")
        return
    lines = r.stdout.splitlines()

    # Percentile-based rank markers between `<<<===` and the percent:
    #   ***  → top 5%   of unique sampled PCs (the hottest band)
    #   **   → top 10%  (rank between 5% and 10%)
    #   *    → top 20%  (rank between 10% and 20%)
    # Cutoffs scale with the size of the histogram, so a small kernel
    # with 20 unique sampled PCs gets `***` on its top 1, while a
    # large kernel with 5000 unique PCs gets `***` on its top ~250.
    # Ties at boundaries can include slightly more than the nominal
    # cutoff (we sort then assign rank by index) — that's fine; the
    # goal is "is this in the top tier" not exact rank precision.
    ranked = sorted(offset_counts.items(), key=lambda kv: -kv[1])
    n = len(ranked)
    # ceil(n * pct) without pulling in math, so empty histograms and
    # very small N still get sensible cutoffs (a 1-PC histogram puts
    # *** on its only entry).
    n5 = -(-n * 5 // 100)
    n10 = -(-n * 10 // 100)
    n20 = -(-n * 20 // 100)
    rank_marker = {}
    for i, (offset, _) in enumerate(ranked):
        if i < n5:
            rank_marker[offset] = "***"
        elif i < n10:
            rank_marker[offset] = "**"
        elif i < n20:
            rank_marker[offset] = "*"
        else:
            break

    # Annotate each OMInstrumentPoint call site with which op it
    # brackets, sourced from `__omip:` DWARF subprograms. Silent
    # no-op if the .so wasn't built with --profile-ir.
    omip_pc_info = {}
    spans, _ = extract_op_spans_via_dwarf(so_path)
    for s in spans:
        omip_pc_info[s["begin_pc"]] = ("begin", s["op_name"], s["node_name"])
        omip_pc_info[s["end_pc"]] = ("end", s["op_name"], s["node_name"])

    # Function header, e.g. "0000000000001dc0 <_init>:"
    header_re = re.compile(r"^[0-9a-f]+\s+<([^>]+)>:\s*$", re.IGNORECASE)

    # First pass: split into (preamble, [functions]), accumulating each
    # function's sample total so we can sort+filter before writing.
    preamble_lines = []
    funcs = []  # list of [label, [line indices], total_samples]
    cur = None
    for i, line in enumerate(lines):
        h = header_re.match(line)
        if h:
            if cur is not None:
                funcs.append(cur)
            cur = [h.group(1), [i], 0]
            continue
        if cur is None:
            preamble_lines.append(i)
            continue
        cur[1].append(i)
        m = _INSTR_RE.match(line)
        if m:
            offset = int(m.group(1), 16) - load_addr
            cur[2] += offset_counts.get(offset, 0)
    if cur is not None:
        funcs.append(cur)

    total = sum(offset_counts.values())
    hot = [f for f in funcs if f[2] > 0]
    hot.sort(key=lambda f: -f[2])

    with open(output_path, "w") as fh:
        fh.write(f"; Annotated disassembly of {so_path}\n")
        fh.write(f"; Total mapped samples: {total}\n")
        fh.write(
            f"; Showing {len(hot)} of {len(funcs)} functions "
            "(only those with sampled instructions).\n;\n"
        )
        # Reader's guide. Search for "<<<===" to jump straight to the
        # next sampled instruction. The star-rank decoration before the
        # percentage tells you how hot this single PC is across the
        # whole .so:
        #   ***   top 5 hottest instructions
        #   **    top 10 (rank 6-10)
        #   *     top 25 (rank 11-25)
        #   (none) sampled but outside the top 25
        # The percentage is share of all mapped samples; the
        # parenthesized number after it is the absolute sample count
        # (so a "0.00% (1)" line is a single-sample hit that still
        # made the top-25 cutoff by absolute count).
        fh.write(
            "; Search marker: `<<<===` jumps to the next sampled " "instruction.\n"
        )
        fh.write(f"; Star ranks (over {n} unique sampled PCs in the .so):\n")
        fh.write(f";   ***   top  5%  ({n5} PCs)\n")
        fh.write(
            f";   **    top 10%  (next {max(0, n10 - n5)} PCs, ranks "
            f"{n5 + 1 if n5 else 0}-{n10})\n"
        )
        fh.write(
            f";   *     top 20%  (next {max(0, n20 - n10)} PCs, ranks "
            f"{n10 + 1 if n10 else 0}-{n20})\n"
        )
        fh.write(";   (none) sampled, but outside the top 20%\n")
        fh.write(
            "; Format: `<<<=== [stars] PCT% (COUNT)` — PCT is "
            "share of total mapped samples,\n"
        )
        fh.write(";         COUNT is the absolute sample hits at this PC.\n;\n")
        fh.write("; Functions by sample share (decreasing):\n")
        for label, _, t in hot:
            pct = 100.0 * t / total
            fh.write(f";   {pct:6.2f}%  ({t:8d})  {label}\n")
        fh.write("\n")

        banner = "; " + "=" * 60 + "\n"
        for label, idxs, t in hot:
            pct = 100.0 * t / total
            fh.write("\n" + banner)
            fh.write(f"; {label}\n")
            fh.write(f";   {t} samples ({pct:.2f}% of total)\n")
            fh.write(banner)
            for i in idxs:
                line = lines[i]
                m = _INSTR_RE.match(line)
                if m:
                    addr = int(m.group(1), 16)
                    offset = addr - load_addr
                    # Emit a BEGIN/END marker comment BEFORE the call
                    # instruction so reading top-to-bottom shows op
                    # boundaries in the natural execution order.
                    info = omip_pc_info.get(addr)
                    if info:
                        kind, op_name, node_name = info
                        arrow = ">>> BEGIN" if kind == "begin" else "<<< END  "
                        fh.write(f"; {arrow}  {op_name}   ({node_name})\n")
                    c = offset_counts.get(offset, 0)
                    if c > 0:
                        ipct = 100.0 * c / total
                        marker = rank_marker.get(offset)
                        suffix = f" {marker}" if marker else ""
                        # Pad so the annotation always starts in the
                        # same column. expandtabs(8) makes the length
                        # match the visual width of the objdump line
                        # (which uses tabs at 8-col stops).
                        base = line.rstrip().expandtabs(8)
                        pad_to = max(len(base), 60)
                        # Append the absolute count too — at 2 decimals
                        # of percent, a line with 1 sample still shows
                        # "0.00%", which can look like a 0-sample
                        # annotation. Including the count removes the
                        # ambiguity.
                        line = (
                            base.ljust(pad_to) + f"  <<<==={suffix} {ipct:.2f}% ({c})"
                        )
                fh.write(line + "\n")
    print(
        f"  wrote {len(hot)} functions, " f"{sum(f[2] for f in hot)} samples annotated"
    )


def print_instruction_mix(mnem_counts, mapped_total, unmapped_total, limit=40):
    if not mnem_counts:
        print("  (no instruction samples to report)")
        return
    print(
        f"\nInstruction mix inside model.so "
        f"(mapped {mapped_total} / unmapped {unmapped_total}):"
    )
    print(f"  {'#':>3}  {'count':>10}  {'pct':>6}  mnemonic")
    items = sorted(mnem_counts.items(), key=lambda kv: -kv[1])
    denom = mapped_total or 1
    for i, (mnem, c) in enumerate(items[:limit], 1):
        print(f"  {i:>3}  {c:>10}  {100.0 * c / denom:5.1f}%  {mnem}")
    if len(items) > limit:
        rest = sum(c for _, c in items[limit:])
        print(
            f"  ... {len(items) - limit} more mnemonics, "
            f"{rest} samples ({100.0 * rest / denom:.1f}%)"
        )


# Per-ONNX-op attribution. Each `OMInstrumentPoint` call site emitted
# by onnx-mlir's KrnlInstrument lowering carries a DISubprogram named
# `__omip:<opName>:<nodeName>`. We recover op spans by reading those
# DIEs via `llvm-dwarfdump` — see extract_op_spans_via_dwarf below.


# DWARF-based op-span recovery -----------------------------------------
#
# Recent onnx-mlir builds (with the KrnlInstrument lowering pass that
# attaches a DISubprogramAttr to each OMInstrumentPoint call) emit a
# `DW_TAG_subprogram` DIE with name `__omip:<opName>:<nodeName>` and an
# explicit DW_AT_low_pc / DW_AT_high_pc range covering the call site.
# When those DIEs are present we read them via `llvm-dwarfdump`,
# producing op spans directly — no per-arch register dataflow, no
# .rodata C-string reads, no risk of misattribution between
# `onnx.<X>` and `zhigh.<X>` due to symbol-name vs string-content
# drift. The compiler chose the name AT lowering time, after the
# ZHigh conversion pass, so it always reflects the post-conversion
# op identity.
#
# Each ONNX op produces TWO subprograms (one per OMInstrumentPoint
# call: begin and end), both with the same name. We sort by PC and
# pair adjacent same-name subprograms into (begin, end) op spans.

# Subprograms emitted by KrnlInstrument.cpp use this prefix on their
# DW_AT_name. Anything else in the .so (regular C/C++ code, helper
# functions) is filtered out.
_OMIP_DWARF_PREFIX = "__omip:"

_DWARF_TAG_RE = re.compile(r"^\s*0x[0-9a-f]+:\s+DW_TAG_(\w+)", re.IGNORECASE)
_DWARF_NAME_RE = re.compile(r'DW_AT_(?:linkage_)?name\s+\("([^"]+)"\)', re.IGNORECASE)
_DWARF_LOW_PC_RE = re.compile(r"DW_AT_low_pc\s+\(0x([0-9a-fA-F]+)\)", re.IGNORECASE)
_DWARF_HIGH_PC_RE = re.compile(r"DW_AT_high_pc\s+\(0x([0-9a-fA-F]+)\)", re.IGNORECASE)
# `DW_AT_abstract_origin (0x.... "name")` — llvm-dwarfdump resolves the
# offset to the referenced DIE's name in the same line.
_DWARF_ABSTRACT_ORIGIN_RE = re.compile(
    r'DW_AT_abstract_origin\s+\([^"]*"([^"]+)"\)', re.IGNORECASE
)


def _read_omip_subprograms(so_path):
    """Walk `llvm-dwarfdump --debug-info` and return a list of
    {name, low_pc, high_pc} dicts for every DWARF entry whose effective
    name starts with `__omip:` and that carries a PC range.

    The KrnlInstrument lowering emits each call site as a
    `CallSiteLoc(callee=fused<DISubprogram>, caller=fused<funcDISP>)`,
    which mlir-translate turns into an `inlinedAt` chain. After codegen,
    the abstract `__omip:<opName>:<nodeName>` `DW_TAG_subprogram` has
    no PC range (it's marked `DW_AT_inline = DW_INL_inlined`); the
    PC range lives on the corresponding `DW_TAG_inlined_subroutine`
    DIE whose `DW_AT_abstract_origin` resolves back to that subprogram.
    We pick up either form: a concrete subprogram whose name starts
    with `__omip:`, OR an inlined_subroutine whose abstract_origin
    resolves to one. Returns [] if llvm-dwarfdump isn't available, the
    binary has no debug info, or no `__omip:` markers are present.

    On macOS, `clang -g` emits DWARF to a sibling `<binary>.dSYM/`
    bundle rather than embedding it in the .so itself. The bundle is
    a directory tree; the actual Mach-O carrying the DWARF lives at
    `<binary>.dSYM/Contents/Resources/DWARF/<basename>`. We point
    llvm-dwarfdump at that inner file directly because some versions
    don't auto-resolve when given just the bundle root. Linux keeps
    debug info directly in the .so.
    """
    target = so_path
    dsym = so_path + ".dSYM"
    if os.path.isdir(dsym):
        inner = os.path.join(
            dsym, "Contents", "Resources", "DWARF", os.path.basename(so_path)
        )
        target = inner if os.path.isfile(inner) else dsym
    try:
        r = subprocess.run(
            ["llvm-dwarfdump", "--debug-info", target], capture_output=True, text=True
        )
    except FileNotFoundError:
        return []
    if r.returncode != 0:
        return []

    subprograms = []
    cur = None  # in-progress DIE dict (or None outside a relevant DIE)

    def flush():
        nonlocal cur
        if (
            cur is not None
            and cur.get("name", "").startswith(_OMIP_DWARF_PREFIX)
            and "low_pc" in cur
            and "high_pc" in cur
        ):
            low = cur["low_pc"]
            high = cur["high_pc"]
            # DW_AT_high_pc is either an absolute address (DWARF v2/v3)
            # or an offset from low_pc (DWARF v4+). Detect by magnitude:
            # if it's smaller than low_pc, it must be an offset.
            if high < low:
                high = low + high
            subprograms.append({"name": cur["name"], "low_pc": low, "high_pc": high})
        cur = None

    for line in r.stdout.splitlines():
        m = _DWARF_TAG_RE.match(line)
        if m:
            flush()
            tag = m.group(1).lower()
            if tag in ("subprogram", "inlined_subroutine"):
                cur = {}
            continue
        if cur is None:
            continue
        if (m := _DWARF_NAME_RE.search(line)) and "name" not in cur:
            # Take the FIRST DW_AT_name (DW_AT_linkage_name follows
            # right after with the same content for our markers).
            cur["name"] = m.group(1)
            continue
        if (m := _DWARF_ABSTRACT_ORIGIN_RE.search(line)) and "name" not in cur:
            # Inlined subroutines don't carry their own DW_AT_name —
            # llvm-dwarfdump prints the resolved referent's name on the
            # same line as `DW_AT_abstract_origin`. That's where the PC
            # range lives for our `__omip:` markers.
            cur["name"] = m.group(1)
            continue
        if m := _DWARF_LOW_PC_RE.search(line):
            cur["low_pc"] = int(m.group(1), 16)
            continue
        if m := _DWARF_HIGH_PC_RE.search(line):
            cur["high_pc"] = int(m.group(1), 16)
            continue
    flush()

    return subprograms


def extract_op_spans_via_dwarf(so_path):
    """Build op spans from `__omip:` DWARF subprograms emitted by the
    onnx-mlir KrnlInstrument lowering pass. Replaces the per-arch
    register-dataflow recovery (`extract_omip_calls`) when DWARF
    markers are present. Returns ([], 0) when no markers are found
    (signaling the caller to fall back).

    Each ONNX op invocation produces two adjacent subprograms (one
    per OMInstrumentPoint begin/end call) sharing the same name. We
    sort by PC and pair them; the op body sits between them at
    [begin.high_pc, end.low_pc).
    """
    subs = _read_omip_subprograms(so_path)
    if not subs:
        return [], 0

    subs.sort(key=lambda s: s["low_pc"])
    spans = []
    unpaired = 0
    i = 0
    while i < len(subs):
        a = subs[i]
        if i + 1 < len(subs) and subs[i + 1]["name"] == a["name"]:
            b = subs[i + 1]
            # Strip the `__omip:` prefix and split at the first ':'
            # to recover (op_name, node_name). Node names contain '/'
            # but never ':', so a single split is unambiguous.
            payload = a["name"][len(_OMIP_DWARF_PREFIX) :]
            sep = payload.find(":")
            if sep < 0:
                unpaired += 2
                i += 2
                continue
            op_name = payload[:sep]
            node_name = payload[sep + 1 :]
            spans.append(
                {
                    "op_name": op_name,
                    "node_name": node_name,
                    "begin_pc": a["low_pc"],
                    "end_pc": b["low_pc"],
                    # `body_pc` is the first instruction strictly after the
                    # begin call — i.e. just past the end of the begin
                    # subprogram's PC range.
                    "body_pc": a["high_pc"],
                }
            )
            i += 2
        else:
            unpaired += 1
            i += 1
    return spans, unpaired


def filter_offsets_by_op_regex(offset_counts, spans, load_addr, op_regex, invert=False):
    """Return the subset of offset_counts whose PCs fall inside an op
    span whose op_name matches `op_regex` (re.search, so a substring
    pattern works without `.*` boilerplate). When `invert` is True,
    keep spans whose op_name does NOT match instead — used by
    `--not-op REGEX` to exclude families of ops from the mix.

    Also returns the list of matched spans and the set of distinct
    matching op names so the caller can tell the user what got selected.
    """
    flag_name = "--not-op" if invert else "--op"
    try:
        pat = re.compile(op_regex)
    except re.error as e:
        sys.exit(f"Error: {flag_name} regex {op_regex!r} is invalid: {e}")
    if invert:
        matched = [s for s in spans if not pat.search(s["op_name"])]
    else:
        matched = [s for s in spans if pat.search(s["op_name"])]
    if not matched:
        return {}, [], set()

    matched_sorted = sorted(matched, key=lambda s: s["body_pc"])
    body_pcs = [s["body_pc"] for s in matched_sorted]
    end_pcs = [s["end_pc"] for s in matched_sorted]

    import bisect

    out = {}
    for offset, count in offset_counts.items():
        addr = offset + load_addr
        idx = bisect.bisect_right(body_pcs, addr) - 1
        if idx < 0 or addr >= end_pcs[idx]:
            continue
        out[offset] = count
    matched_names = {s["op_name"] for s in matched}
    return out, matched_sorted, matched_names


def attribute_offsets_to_ops(
    offset_counts,
    spans,
    load_addr,
    ordered_samples=None,
    kernel_leaves=None,
):
    """Attribute each sample to an ONNX op span.

    Three input modes, in order of accuracy:

    1. `ordered_samples` (v2 inproc dump): the runtime stamped each
       sample with `OMCurrentOpName` — exact, even for samples that
       landed in a callee outside main_graph (e.g. zDNN's
       `invoke_nnpa` for s390x NNPA). The tag's AFTER bit flags
       samples that fell after an op finished but before the next
       began; those go to `unattributed`.

    2. `kernel_leaves` (kernel-side `sample` / `perf` paths): no
       runtime markers, but the recorded call stacks let us recover
       the calling op when the leaf PC misses every span — we walk
       the deepest-first ancestor chain looking for an in-span PC.
       The leaf offset is still used for the per-op instruction mix
       so the disassembly view stays accurate.

    3. Spatial-only (replayed JSON profile, or no extra info): index
       each leaf offset against the DWARF spans. Samples in callees
       outside main_graph end up in `unattributed`.

    Returns:
      per_op: dict[op_name -> total samples]
      per_op_offsets: dict[op_name -> dict[offset -> count]] for a
        later instruction-mix breakdown
      attributed_total, unattributed_total
    """
    if not spans and ordered_samples is None and kernel_leaves is None:
        return {}, {}, 0, sum(offset_counts.values())

    per_op = {}
    per_op_offsets = {}
    attributed = 0
    unattributed = 0

    if ordered_samples is not None:
        # Marker-based attribution: the runtime told us which op was
        # active at sample time, so we just trust it.
        for s in ordered_samples:
            op = s["op_name"]
            tag = s["tag"]
            after = bool(tag & INPROC_TAG_AFTER_BIT)
            if op is None or after:
                # No op active, or the most recent boundary was an
                # `after` marker — we're between ops. Don't credit
                # any op for this sample.
                unattributed += 1
                continue
            offset = s["offset"]
            per_op[op] = per_op.get(op, 0) + 1
            d = per_op_offsets.setdefault(op, {})
            d[offset] = d.get(offset, 0) + 1
            attributed += 1
        return per_op, per_op_offsets, attributed, unattributed

    # Spatial paths share the binary-search index over span bodies.
    spans_sorted = sorted(spans, key=lambda s: s["body_pc"])
    body_pcs = [s["body_pc"] for s in spans_sorted]
    end_pcs = [s["end_pc"] for s in spans_sorted]
    op_names = [s["op_name"] for s in spans_sorted]

    import bisect

    def lookup_op(offset):
        addr = offset + load_addr
        idx = bisect.bisect_right(body_pcs, addr) - 1
        if idx < 0 or addr >= end_pcs[idx]:
            return None
        return op_names[idx]

    if kernel_leaves is not None:
        # Stack-walk attribution: try the leaf PC first (the
        # disassembly view's preferred PC); on a miss, walk the
        # recorded call-stack ancestors deepest-first looking for an
        # in-span PC. The leaf offset still drives `per_op_offsets`
        # so the per-op instruction-mix stays keyed off where the
        # CPU actually spent the cycle, not the calling site.
        for leaf in kernel_leaves:
            offset = leaf["offset"]
            count = leaf["count"]
            op = lookup_op(offset)
            if op is None:
                for anc in leaf["ancestors"]:
                    op = lookup_op(anc)
                    if op is not None:
                        break
            if op is None:
                unattributed += count
                continue
            per_op[op] = per_op.get(op, 0) + count
            d = per_op_offsets.setdefault(op, {})
            d[offset] = d.get(offset, 0) + count
            attributed += count
        return per_op, per_op_offsets, attributed, unattributed

    # Spatial-only fallback (e.g. replayed JSON profile).
    for offset, count in offset_counts.items():
        op = lookup_op(offset)
        if op is None:
            unattributed += count
            continue
        per_op[op] = per_op.get(op, 0) + count
        d = per_op_offsets.setdefault(op, {})
        d[offset] = d.get(offset, 0) + count
        attributed += count
    return per_op, per_op_offsets, attributed, unattributed


def print_per_op_report(per_op_counts, attributed, unattributed, limit=40):
    if not per_op_counts:
        print(
            "\n(no per-op attribution; OMInstrumentPoint markers not "
            "found — was the model compiled with --profile-ir?)"
        )
        return
    print(
        f"\nPer-ONNX-op samples "
        f"(attributed {attributed} / unattributed {unattributed}):"
    )
    print(f"  {'#':>3}  {'count':>10}  {'pct':>6}  op")
    items = sorted(per_op_counts.items(), key=lambda kv: -kv[1])
    denom = attributed or 1
    for i, (op, c) in enumerate(items[:limit], 1):
        print(f"  {i:>3}  {c:>10}  {100.0 * c / denom:5.1f}%  {op}")
    if len(items) > limit:
        rest = sum(c for _, c in items[limit:])
        print(
            f"  ... {len(items) - limit} more ops, "
            f"{rest} samples ({100.0 * rest / denom:.1f}%)"
        )


def print_per_op_instruction_mix(
    per_op_offsets, instructions, load_addr, top_ops=8, top_mnems=8
):
    """For the top-K ops by sample count, show their instruction mix."""
    if not per_op_offsets:
        return
    op_totals = sorted(
        ((op, sum(d.values())) for op, d in per_op_offsets.items()),
        key=lambda kv: -kv[1],
    )[:top_ops]
    print(f"\nTop {len(op_totals)} ops — instruction mix:")
    for op, total in op_totals:
        offsets = per_op_offsets[op]
        mnem_counts = {}
        for off, c in offsets.items():
            entry = instructions.get(off + load_addr)
            if entry is None:
                continue
            mnem = entry[0] if isinstance(entry, tuple) else entry
            base = mnem.split(".")[0]
            mnem_counts[base] = mnem_counts.get(base, 0) + c
        items = sorted(mnem_counts.items(), key=lambda kv: -kv[1])
        denom = sum(mnem_counts.values()) or 1
        head = ", ".join(f"{m}={c}" for m, c in items[:top_mnems])
        if len(items) > top_mnems:
            head += f", +{len(items) - top_mnems} more"
        print(f"  {op:>32s}  total={total:>8d}   {head}")


def save_profile(
    out_path,
    cpp_path,
    binary_path,
    model_so,
    duration,
    total_samples,
    inside_samples,
    per_binary_counts,
    top_inside,
    runtime_base,
    load_addr,
    offset_counts,
    sampler,
):
    payload = {
        "version": PROFILE_FORMAT_VERSION,
        "so_path": os.path.abspath(model_so),
        "script": os.path.abspath(cpp_path),
        "binary": os.path.abspath(binary_path),
        "duration": duration,
        "timestamp": datetime.datetime.now().isoformat(timespec="seconds"),
        "inside_marker": INSIDE_MARKER,
        # The resolved sampler name (one of `sample`, `perf`, `inproc`
        # — never `auto`); recorded so re-analysis later can tell what
        # backend produced the data and which biases to expect (e.g.
        # `sample`'s aggregation blur on the per-PC histogram).
        "sampler": sampler,
        "total_samples": total_samples,
        "inside_samples": inside_samples,
        "per_binary_counts": per_binary_counts,
        # JSON keys must be strings; flatten (symbol, binary) tuples.
        "top_inside_symbols": [
            {"symbol": sym, "binary": b, "count": c} for (sym, b), c in top_inside
        ],
        # Captured for the instruction-mix step in --analyze: runtime
        # base lets us turn sampled PCs into in-image offsets, and
        # load_addr (almost always 0) shifts those onto the addresses
        # objdump prints.
        "so_runtime_base": f"0x{runtime_base:x}",
        "so_load_address": load_addr,
        # Decimal-string keys (JSON requirement); values are sample
        # weights distributed across visible PCs by the call-graph walk.
        "offset_counts": {str(k): v for k, v in offset_counts.items()},
    }
    with open(out_path, "w") as f:
        json.dump(payload, f, indent=2)
    print(f"\nWrote profile: {out_path}")


def print_report(total_samples, inside_samples, per_binary_counts, top_inside):
    if total_samples:
        outside = total_samples - inside_samples
        in_pct = 100.0 * inside_samples / total_samples
        out_pct = 100.0 * outside / total_samples
        print(f"\nSamples (using `{INSIDE_MARKER}` as inside-model marker):")
        print(f"  total          : {total_samples}")
        print(f"  inside model   : {inside_samples} ({in_pct:.1f}%)")
        print(f"  outside model  : {outside} ({out_pct:.1f}%)")
    else:
        print(
            f"\nSamples: total unknown (no Thread_ lines found); "
            f"inside={inside_samples}"
        )

    if per_binary_counts:
        items = sorted(per_binary_counts.items(), key=lambda kv: -kv[1])
        denom = sum(per_binary_counts.values()) or 1
        print(f"\nPer-binary leaf samples (decreasing):")
        print(f"  {'#':>3}  {'count':>8}  {'pct':>6}  binary")
        for i, (b, c) in enumerate(items, 1):
            print(f"  {i:>3}  {c:>8}  {100.0 * c / denom:5.1f}%  {b}")

    if top_inside:
        denom = sum(c for _, c in top_inside) or 1
        print(f"\nTop frames inside `{INSIDE_MARKER}` subtree (leaf samples):")
        print(f"  {'#':>3}  {'count':>8}  {'pct':>6}  symbol  (binary)")
        for i, ((sym, b), c) in enumerate(top_inside, 1):
            print(f"  {i:>3}  {c:>8}  {100.0 * c / denom:5.1f}%  " f"{sym}  ({b})")


def analyze_profile(
    json_path,
    so_override=None,
    op_regex=None,
    not_op_regex=None,
    annotate_path=None,
    debug_omip=False,
):
    """Re-print the report from a saved JSON profile, plus the
    instruction-mix breakdown derived from disassembling the .so.
    `so_override` lets you re-disassemble a different copy of the .so
    (e.g. one with debug info preserved) without re-recording.
    `op_regex` / `not_op_regex` (mutually exclusive) switch the
    instruction-mix output to a filtered-by-op-name view (include vs.
    exclude) and suppress the per-op sections.
    """
    if not os.path.exists(json_path):
        sys.exit(f"Error: profile JSON not found: {json_path}")
    with open(json_path) as f:
        data = json.load(f)

    print(f"Analyzing: {json_path}")
    print(f"  recorded against : {data.get('so_path')}")
    print(f"  duration         : {data.get('duration')}s")
    print(f"  inside marker    : {data.get('inside_marker', INSIDE_MARKER)}")

    top_inside = [
        ((e["symbol"], e["binary"]), e["count"])
        for e in data.get("top_inside_symbols", [])
    ]
    print_report(
        data.get("total_samples", 0),
        data.get("inside_samples", 0),
        data.get("per_binary_counts", {}),
        top_inside,
    )

    # Instruction-mix step. Skip silently for old (v1/v2) JSONs that
    # didn't capture per-PC counts.
    offset_counts_raw = data.get("offset_counts")
    if not offset_counts_raw:
        print(
            "\n(no offset_counts in this profile — re-record to get the "
            "instruction-mix breakdown.)"
        )
        return
    offset_counts = {int(k): v for k, v in offset_counts_raw.items()}
    so_path = so_override or data.get("so_path")
    if not so_path or not os.path.exists(so_path):
        print(f"\nWarning: .so not found ({so_path!r}); skipping instruction mix.")
        return
    load_addr = data.get("so_load_address", 0)
    instructions = disassemble_so(so_path)
    if not instructions:
        return
    # Same gating as run mode: when --op or --not-op is set, the
    # filtered instruction mix inside run_per_op_analysis replaces the
    # unfiltered one and the per-op breakdown is skipped.
    if op_regex is None and not_op_regex is None:
        mnem_counts, mapped, unmapped = map_offsets_to_mnemonics(
            offset_counts, instructions, load_addr
        )
        print_instruction_mix(mnem_counts, mapped, unmapped)
    # Replayed JSON profiles don't carry firing order or call-stack
    # ancestors; per-op attribution falls back to spatial-only inside
    # attribute_offsets_to_ops.
    run_per_op_analysis(
        so_path,
        offset_counts,
        instructions,
        load_addr,
        op_regex=op_regex,
        not_op_regex=not_op_regex,
        debug_omip=debug_omip,
        ordered_samples=None,
        kernel_leaves=None,
    )
    if annotate_path:
        write_annotated_disasm(so_path, offset_counts, load_addr, annotate_path)


def run_per_op_analysis(
    so_path,
    offset_counts,
    instructions,
    load_addr,
    op_regex=None,
    not_op_regex=None,
    debug_omip=False,
    ordered_samples=None,
    kernel_leaves=None,
):
    """Produce the per-ONNX-op breakdown by reading `__omip:` DWARF
    subprograms emitted by onnx-mlir's KrnlInstrument lowering pass.
    Silent no-op when no markers are present (i.e. the .so wasn't
    built with --profile-ir against a recent enough onnx-mlir).

    With `op_regex` / `not_op_regex`: print only an instruction mix
    restricted to PCs inside op spans whose name matches (or doesn't
    match) the regex; suppress the per-op summary + top-N mix.
    """
    assert not (
        op_regex and not_op_regex
    ), "op_regex and not_op_regex are mutually exclusive"
    spans, unpaired = extract_op_spans_via_dwarf(so_path)
    if not spans:
        if op_regex or not_op_regex:
            sys.exit(
                "Error: --op / --not-op require `__omip:` DWARF markers "
                "which weren't found in the .so. Recompile with a recent "
                "onnx-mlir + --profile-ir."
            )
        return
    print(
        f"\nOMInstrumentPoint markers (DWARF): {len(spans)} op spans, "
        f"{unpaired} unpaired"
    )

    if debug_omip:
        # List every distinct op name recovered with span counts and
        # a few example PC ranges. Useful when you want to cross-check
        # the recovery against the source MLIR or post-instrument IR.
        from collections import defaultdict

        by_name = defaultdict(list)
        for s in spans:
            by_name[s["op_name"]].append(s)
        print(f"\n[--debug-omip] {len(by_name)} distinct op names recovered:")
        for name, ss in sorted(by_name.items(), key=lambda kv: -len(kv[1])):
            print(f"  {len(ss):6d} spans  {name!r}")
            for s in ss[:3]:
                print(
                    f"      pc=0x{s['begin_pc']:x}..0x{s['end_pc']:x}  "
                    f"node={s['node_name']!r}"
                )
            if len(ss) > 3:
                print(f"      ... +{len(ss) - 3} more")

    active_regex = op_regex if op_regex else not_op_regex
    if active_regex is not None:
        invert = not_op_regex is not None
        flag = "--not-op" if invert else "--op"
        filtered, matched_spans, matched_names = filter_offsets_by_op_regex(
            offset_counts, spans, load_addr, active_regex, invert=invert
        )
        all_names = sorted({s["op_name"] for s in spans})
        if not matched_spans:
            verb = "excluded everything" if invert else "no op span name matched"
            print(
                f"\n{flag} {active_regex!r}: {verb}. "
                f"Available op names include: "
                f"{', '.join(all_names[:8])}..."
            )
            return
        names_label = "kept" if invert else "matched"
        names_str = ", ".join(sorted(matched_names))
        print(
            f"\n{flag} {active_regex!r} {names_label}: {names_str} "
            f"({len(matched_spans)} spans, "
            f"{sum(filtered.values())} samples)"
        )
        mnem_counts, mapped, unmapped = map_offsets_to_mnemonics(
            filtered, instructions, load_addr
        )
        print_instruction_mix(mnem_counts, mapped, unmapped)
        return

    per_op, per_op_offsets, attributed, unattributed = attribute_offsets_to_ops(
        offset_counts,
        spans,
        load_addr,
        ordered_samples=ordered_samples,
        kernel_leaves=kernel_leaves,
    )
    print_per_op_report(per_op, attributed, unattributed)
    print_per_op_instruction_mix(per_op_offsets, instructions, load_addr)


